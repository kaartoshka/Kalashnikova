x = float(input("Введите пробег в первый день (в километрах): "))
y = float(input("Введите целевой пробег (в километрах): "))
def f(x, y):
    d = 1
    dist = x
    while dist < y:
        dist *= 1.1
        d += 1
    return d
d = f(x, y)
print('Пробег спортсмена составит не менее', y,'километров на', d, 'день')
